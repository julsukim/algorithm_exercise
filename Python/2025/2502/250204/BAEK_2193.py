N = int(input())

"""
1 : 1
2 : 10
3 : 101, 100
4 : 1010, 1001, 1000
5 : 10101, 10100, 10010, 10000, 10001
6 : 101010 / 101000, 101001 / 100100, 100101 / 100000, 100001 / 100010
"""

if N == 1 or N == 2:
    print(1)
else:
    dp = [0]*(N+1)
    dp[1] = 1
    dp[2] = 1

    for i in range(3, N+1):
        dp[i] = dp[i-1] + dp[i-2]

    print(dp[N])
